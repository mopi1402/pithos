// INTENTIONAL: any type needed for dynamic composition
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function pipe(a: any, ...fns: ReadonlyArray<(x: any) => any>): any {
  // INTENTIONAL: Inline cases 0-5 for performance (~2x faster than loop).
  switch (fns.length) {
    case 0:
      return a;
    case 1:
      return fns[0](a);
    case 2:
      return fns[1](fns[0](a));
    case 3:
      return fns[2](fns[1](fns[0](a)));
    case 4:
      return fns[3](fns[2](fns[1](fns[0](a))));
    // Stryker disable next-line ConditionalExpression: performance optimization, loop fallback produces same result
    case 5:
      return fns[4](fns[3](fns[2](fns[1](fns[0](a)))));
  }

  let result = a;
  for (const fn of fns) {
    result = fn(result);
  }
  return result;
}
