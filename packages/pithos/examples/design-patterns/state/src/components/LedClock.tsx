export function LedClock({ value, width = 280, height = 80 }: LedClockProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const dpr = window.devicePixelRatio || 1;
    canvas.width = width * dpr;
    canvas.height = height * dpr;
    ctx.scale(dpr, dpr);
    ctx.clearRect(0, 0, width, height);

    const chars = value.split("");
    const charW = COLS * DOT_GAP;
    const charGap = DOT_GAP * 2;

    const totalW = chars.length * charW + (chars.length - 1) * charGap;
    let startX = (width - totalW) / 2;
    const startY = (height - ROWS * DOT_GAP) / 2;

    for (const ch of chars) {
      const matrix = FONT[ch];
      if (!matrix) { startX += charW + charGap; continue; }

      for (let row = 0; row < ROWS; row++) {
        for (let col = 0; col < COLS; col++) {
          const on = matrix[row][col] === 1;
          const cx = startX + col * DOT_GAP + DOT_GAP / 2;
          const cy = startY + row * DOT_GAP + DOT_GAP / 2;

          ctx.beginPath();
          ctx.arc(cx, cy, DOT_R, 0, Math.PI * 2);
          ctx.fillStyle = on ? ON : OFF;
          ctx.fill();

          if (on) {
            ctx.beginPath();
            ctx.arc(cx, cy, DOT_R * 2.5, 0, Math.PI * 2);
            ctx.fillStyle = "rgba(212,160,23,0.12)";
            ctx.fill();
          }
        }
      }

      startX += charW + charGap;
    }
  }, [value, width, height]);

  return <canvas ref={canvasRef} style={{ width, height }} />;
}
